long	ft_atoi_l(const char *s)
{
	long	r;
	long	sign;

	r = 0;
	if (*s == '\0')
		return (0);
	while (*s == 32 || (*s >= 9 && *s <= 13))
		s++;
	sign = (*s == '-') ? -1 : 1;
	(*s == '-' || *s == '+') ? s++ : s;
	while (*s != '\0')
		if (*s >= '0' && *s <= '9')
			r = (r * 10) + (*s++ - 48);
	return (r * sign);
}
