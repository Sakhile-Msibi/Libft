#include <string.h>

char	*ft_strrchr(const char *s, int c)
{
	char *s1;

	s1 = NULL;
	while (*s)
	{
		if (*s == (char)c)
			s1 = (char *)s;
		s++;
	}
	if (*s == (char)c)
		s1 = (char *)s;
	return (s1);
}
