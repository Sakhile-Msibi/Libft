#include <string.h>

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int i;

	while (n && *s1 && *s1 == *s2)
	{
		s1++;
		s2++;
		n--;
	}
	if (n == 0)
		return (1);
	i = *s1 - *s2;
	if (i == 0)
		return (1);
	else
		return (0);
}
