int		ft_strequ(char const *s1, char const *s2)
{
	int i;

	while (*s1 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	i = *s1 - *s2;
	if (i == 0)
		return (1);
	else
		return (0);
}
