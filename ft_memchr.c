#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*s1;

	s1 = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (*s1 == c)
			return (s1);
		else
			s1++;
		i++;
	}
	return (0);
}
